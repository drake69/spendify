"""Test full LLM-assisted classification pipeline against synthetic files.

Runs classify_document (Flow 2) + normalization on a diverse subset of
~10 synthetic files generated by tests/generate_synthetic_files.py.
Writes results to tests/generated_files/results_llm.csv.

Usage:
    pytest tests/test_llm_import.py -m llm -s -v
    pytest tests/test_llm_import.py -m llm -s -v --tb=short
"""
